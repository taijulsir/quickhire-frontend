const defaultCategories = [
  { name: 'Design', count: 235 },
  { name: 'Sales', count: 756 },
  { name: 'Marketing', count: 140 },
  { name: 'Finance', count: 325 },
  { name: 'Technology', count: 436 },
  { name: 'Engineering', count: 542 },
  { name: 'Business', count: 211 },
  { name: 'Human Resource', count: 346 },
];

export default function CategoriesSection() {
  const [categories, setCategories] = useState<Category[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchCategories = async () => {
      try {
        const response = await jobService.getCategories();
        if (response.success && response.data) {
          const mergedCategories = defaultCategories.map(defaultCat => {
            const apiCat = response.data?.find(c => c.name === defaultCat.name);
            return apiCat ? { ...defaultCat, count: apiCat.count } : defaultCat;
          });
          setCategories(mergedCategories);
        } else {
          setCategories(defaultCategories);
        }
      } catch {
        setCategories(defaultCategories);
      } finally {
        setLoading(false);
      }
    };

    fetchCategories();
  }, []);

  return (
    <section className="bg-white py-16 w-full">
      <div className="max-w-[1440px] mx-auto px-4 sm:px-12 lg:px-20">
        <div className="flex justify-between items-center mb-8">
          <h2 className="text-3xl font-bold text-gray-900">
            Explore by <span className="text-indigo-600">category</span>
          </h2>
          <Link href="/jobs" className="text-indigo-600 hover:text-indigo-700 font-medium flex items-center gap-2">
            Show all jobs
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5l7 7-7 7" />
            </svg>
          </Link>
        </div>
        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-6">
          {loading
            ? Array(8).fill(0).map((_, i) => (
                <div key={i} className="p-6 rounded-lg border border-gray-200 animate-pulse">
                  <div className="w-12 h-12 bg-gray-200 rounded-lg mb-4" />
                  <div className="h-5 bg-gray-200 rounded w-24 mb-2" />
                  <div className="h-4 bg-gray-200 rounded w-32" />
                </div>
              ))
            : categories.map((category, index) => (
                <CategoryCard
                  key={category.name}
                  name={category.name}
                  count={category.count}
                  icon={categoryIcons[category.name]}
                  isActive={index === 2}
                />
              ))}
        </div>
      </div>
    </section>
  );
}
